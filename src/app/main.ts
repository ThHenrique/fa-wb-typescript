import Entrada from "./domain/services/number-text.input";
import CadastroCliente from "./domain/negocio/cadastroCliente";
import ListagemClientes from "./domain/negocio/listagemClientes";

import { BusinessModel } from "./domain/models";


console.log(`Bem-vindo ao cadastro de clientes do Grupo World Beauty`)
let empresa = new BusinessModel()
let execucao = true

while (execucao) {
    console.log(`Opções:`);
    console.log(`1 - Cadastrar cliente`);
    console.log(`2 - Listar todos os clientes`);
    console.log(`0 - Sair`);

    let entrada = new Entrada()
    let opcao = entrada.receberNumero(`Por favor, escolha uma opção: `)

    switch (opcao) {
        case 1:
            let cadastro = new CadastroCliente(empresa.getClientes)
            cadastro.cadastrar()
            break;
        case 2:
            let listagem = new ListagemClientes(empresa.getClientes)
            listagem.listar()
            break;
        case 0:
            execucao = false
            console.log(`Até mais`)
            break;
        default:
            console.log(`Operação não entendida :(`)
    }
}